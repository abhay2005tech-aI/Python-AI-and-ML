# Arithmatic Operator
x = 10
y = 20
print("Addition of x and y is: ", x + y)
print("Subtraction of x and y is: ", x - y)
print("Multiplication of x and y is: ", x * y)
print("Division of x and y is: ", x / y)
print("Modulus of x and y is: ", x % y)
print("Exponent of x and y is: ", x ** y)
print("Floor Division of x and y is: ", x // y)

# Logical Operator
print("x and y is: ", x and y)
print("x or y is: ", x or y)
print("not x is: ", not x)
#Comparison Operator
print("x is equal to y is: ", x == y)
print("x is not equal to y is: ", x != y)
print("x is greater than y is: ", x > y)
print("x is less than y is: ", x < y)
print("x is greater than or equal to y is: ", x >= y)
print("x is less than or equal to y is: ", x <= y)

